/**
 * Look up static pre-translated text first. Returns null if not found
 * (caller should then fall back to the live API for that string/language).
 */
export function getStaticTranslation(text, language) {
  const table = STATIC_TRANSLATIONS[language];
  if (!table) return null;
  return table[text] || null;
}